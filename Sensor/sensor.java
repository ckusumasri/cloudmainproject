package org.cloud.mobilesensor.ui;

public class sensor {

	String sensorname;
	int sensorid;
	int sensorno;
	String sensorlocation;
	String sensortype;
	Boolean sensorused;
	public String getSensorname() {
		return sensorname;
	}
	public void setSensorname(String sensorname) {
		this.sensorname = sensorname;
	}
	public int getSensorid() {
		return sensorid;
	}
	public void setSensorid(int sensorid) {
		this.sensorid = sensorid;
	}
	public int getSensorno() {
		return sensorno;
	}
	public void setSensorno(int sensorno) {
		this.sensorno = sensorno;
	}
	public String getSensorlocation() {
		return sensorlocation;
	}
	public void setSensorlocation(String sensorlocation) {
		this.sensorlocation = sensorlocation;
	}
	public String getSensortype() {
		return sensortype;
	}
	public void setSensortype(String sensortype) {
		this.sensortype = sensortype;
	}
	public Boolean getSensorused() {
		return sensorused;
	}
	public void setSensorused(Boolean sensorused) {
		this.sensorused = sensorused;
	}	
	
}
